Console.WriteLine("Tipos de Herança");


//Tipos de Herança

//Herança Simples é quando uma classe herda de apenas uma classe base.
//Por exemplo, uma classe "Carro" pode herdar de uma classe "Veículo".
//Isso é o mais comum e é suportado por muitas linguagens de programação orientadas a objetos.

//Herança Hierárquica é quando várias classes herdam de uma única classe base.
//Por exemplo, as classes "Cachorro" e "Gato" podem herdar de uma classe "Animal".
//Isso é útil para criar uma estrutura de classes onde várias classes compartilham características comuns
//definidas na classe base.

//Herança Múltipla é quando uma classe herda de mais de uma classe base.
//Por exemplo, uma classe "Smartphone" pode herdar de uma classe "Telefone" e de uma classe "Computador".
//todas as linguagens de programação suportam herança múltipla diretamente, mas algumas, como C++, permitem isso.
//Em linguagens que não suportam herança múltipla, é comum usar interfaces para alcançar um resultado semelhante.
